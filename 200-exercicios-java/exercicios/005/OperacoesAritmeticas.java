public class OperacoesAritmeticas {
    public static void main(String[] args) {
        int num1 = 15;
        int num2 = 10; 

        int soma = num1 + num2;
        int sub = num1 - num2;
        int mult = num1 * num2;
        int div = num1 / num2;
        int mod = num1 % num2;

        System.out.println("Soma: " + soma + " Subtracao: " + sub + " Multiplicacao: " + mult + " Divisao: " + div + " Modulo: " + mod);
    }    
}
